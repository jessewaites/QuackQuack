import Cocoa

class QuackApp: NSObject, NSApplicationDelegate, NSSearchFieldDelegate {
    var statusItem: NSStatusItem!
    var scrollView: NSScrollView?
    var searchField: NSSearchField?
    
    func applicationDidFinishLaunching(_ notification: Notification) {
        statusItem = NSStatusBar.system.statusItem(withLength: NSStatusItem.squareLength)
        statusItem.button?.title = "🦆"
        statusItem.button?.action = #selector(duckClicked)
        statusItem.button?.target = self
    }
    
    @objc func duckClicked() {
        showEmojiSelector()
    }
    
    func showEmojiSelector() {
        showEmojiWindow()
    }
    
    func showEmojiWindow() {
        let window = NSWindow(contentRect: NSRect(x: 0, y: 0, width:500, height: 580),
                             styleMask: [.titled, .closable, .resizable],
                             backing: .buffered,
                             defer: false)
        window.title = "🦆 QuackQuack - Select Emoji"
        window.center()
        
        let contentView = NSView(frame: window.contentView!.bounds)
        window.contentView = contentView
        
        // Add search field at the top using NSSearchField
        let searchField = NSSearchField(frame: NSRect(x: 20, y: 540, width: 460, height: 24))
        searchField.placeholderString = "Search emoji, then click to copy to clipboard"
        searchField.delegate = self
        contentView.addSubview(searchField)
        
        // Use NotificationCenter as backup for text changes
        NotificationCenter.default.addObserver(
            forName: NSControl.textDidChangeNotification,
            object: searchField,
            queue: .main
        ) { [weak self] notification in
            if let searchField = notification.object as? NSSearchField,
               let scrollView = self?.scrollView {
                let searchTerm = searchField.stringValue.lowercased()
                self?.createEmojiGrid(in: scrollView, searchTerm: searchTerm)
            }
        }
        
        // Scroll view for emojis (reduced height to make room for search and credits)
        let scrollView = NSScrollView(frame: NSRect(x: 20, y: 50, width: 460, height: 480))
        scrollView.hasVerticalScroller = true
        scrollView.autohidesScrollers = false
        contentView.addSubview(scrollView)
        
        // Add credits text at the bottom
        let creditsLabel = NSTextField(frame: NSRect(x: 20, y: 20, width: 460, height: 20))
        creditsLabel.stringValue = "Created by Jesse & Christine ❤️"
        creditsLabel.isEditable = false
        creditsLabel.isBordered = false
        creditsLabel.backgroundColor = NSColor.clear
        creditsLabel.textColor = NSColor.secondaryLabelColor
        creditsLabel.font = NSFont.systemFont(ofSize: 12)
        creditsLabel.alignment = .center
        contentView.addSubview(creditsLabel)
        
        // Create emoji grid with all emojis
        createEmojiGrid(in: scrollView, searchTerm: "")
        
        // Store references as instance variables
        self.scrollView = scrollView
        self.searchField = searchField
        
        window.makeKeyAndOrderFront(nil)
        NSApp.activate(ignoringOtherApps: true)
    }
    
    // NSSearchFieldDelegate methods
    func controlTextDidChange(_ obj: Notification) {
        if let searchField = obj.object as? NSSearchField,
           let scrollView = self.scrollView {
            let searchTerm = searchField.stringValue.lowercased()
            createEmojiGrid(in: scrollView, searchTerm: searchTerm)
        }
    }
    
    // Handle the search field's cancel button (X button)
    func searchFieldDidEndSearching(_ sender: NSSearchField) {
        if let scrollView = self.scrollView {
            createEmojiGrid(in: scrollView, searchTerm: "")
        }
    }
    
    
